package Model;

public class Result {
    int id;
    int result;
    long time;

    public Result(int id, int result, long time) {
        this.id = id;
        this.result = result;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getResult() {
        return result;
    }

    public void setResult(int result) {
        this.result = result;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }
}
